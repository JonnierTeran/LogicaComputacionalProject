    // M√©todo para verificar si los campos est√°n diligenciados
    private boolean camposDiligenciados() {
        return !txtnom.getText().isEmpty()
                && !txtApe.getText().isEmpty()
                && !txtEdad.getText().isEmpty()
                && !txtS.getText().isEmpty()
                && !txtP.getText().isEmpty();
    }

    //metodo para activar el boton de cargar informacion
    private void BtnCargarInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCargarInfoActionPerformed

        // Validaci√≥n de formulario diligenciado
    if (camposDiligenciados()) {
        // Generamos Variables y obtenemos la informaci√≥n ingresada en los campos de la vista
        String nombre = this.txtnom.getText();
        String apellido = this.txtApe.getText();
        int edad = Integer.parseInt(this.txtEdad.getText());
        String sexo = this.txtS.getText();
        String profesion = this.txtP.getText();

        // Creamos una instancia/objeto de tipo Persona
        Persona PersonaObj = new Persona(nombre, apellido, edad, sexo, profesion);

        // Informaci√≥n de Registrado
        JOptionPane.showMessageDialog(null, "Informaci√≥n Registrada Exitosamente");

        // Cargamos la informaci√≥n Registrada
        this.TextArea.setText("Informaci√≥n Registrada \n\n" + PersonaObj.toString());
    } else {
        JOptionPane.showMessageDialog(null, "Por favor, complete todos los campos.");
    }
        

    }//GEN-LAST:event_BtnCargarInfoActionPerformed

